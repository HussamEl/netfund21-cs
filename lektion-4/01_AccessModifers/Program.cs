/*
    Access Modifiers (Encapsulation - OOP)
    --------------------------------------------------------------------------------------------------------
    Saker du bör/ska kunna:
        public                  blir accessbar överallt, i alla projekt, relationer och assemblies (Project Reference)
        private                 blir bara accessbar inom sitt kodblock { }
        internal                blir accessbar inom det egna projektet (assembly)

        protected               blir accessbar genom arv

    Special fall:
        protected internal      accessbar via arv och inom det egna projektet (assembly)
        private protected       accessbar via arv och inom sitt kodblock { }

    Saker att känna till:
        En klass kan bara vara public och internal
*/